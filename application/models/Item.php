    public function getDamages(){
        $this->db->select('damages.qty_damaged,damages.date,items.code,items.unitPrice,items.item_image,items.name,items.quantity');
            $this->db->join('items', 'damages.item_code = items.id', 'LEFT');
            $run_q = $this->db->get('damages');

            if($run_q->num_rows() > 0){
                return $run_q->result();
            }
            
            else{
                return FALSE;
            }
    }

