    public function getImageName($itemId){
        $q = "SELECT item_image as 'image' FROM items where id = ?";

        $run_q = $this->db->query($q,[$itemId]);

        if ($run_q->num_rows() > 0) {
            foreach ($run_q->result() as $get) {
                return $get->image;
            }
        }
        else {
            return FALSE;
        }
    }
    
